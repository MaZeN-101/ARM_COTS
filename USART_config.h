/*  								1-UART_EVEN_PARITY      								*/
/*  								2-UART_ODD_PARITY       								*/
/*  																						*/
/********************************************************************************************/
#define UART1_PARITY_MODE				UART_PARITY_DISABLE



/********************************************************************************************/
