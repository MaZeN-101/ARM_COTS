/********************************************************************************************/
/*  						SELECT Transmission Enabled | Disabled :						*/
/*  																						*/
/*  	Options:					0-UART_TRANSMITTER_ENABLE   							*/
/*  								1-UART_TRANSMITTER_DISABLE  							*/
/*  																						*/
/********************************************************************************************/
#define UART1_TRANSMITTER				UART_TRANSMITTER_ENABLE



/********************************************************************************************/
/*  						SELECT Receiver Enabled | Disabled :							*/
/*  																						*/
