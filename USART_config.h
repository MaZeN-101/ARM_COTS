/*  								1-UART_RECEIVER_DISABLE 								*/
/*  																						*/
/********************************************************************************************/
#define UART1_RECEIVER					UART_RECEIVER_ENABLE



/********************************************************************************************/
/*  					SELECT Data Size For Transmission & Reception:  					*/
/*  																						*/
/*  	Options:					0-UART_8BIT_DATA										*/
/*  								1-UART_9BIT_DATA										*/
/*  																						*/
/********************************************************************************************/
